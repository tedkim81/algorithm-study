'''
Y.m.d

[문제설명]

(input)
T: 테스트케이스의 수
[다른 입력값들 설명]

(output)
Case #x: y
[출력값들 설명]

(solution 1)
[문제 해결 방식 설명. 실패하면 solution 2를 생각하여 다시 시도]
'''

def solve(num):
    return num

T = int(input())
for tt in range(1, T+1):
    N = int(input())
    result = solve(N)
    print('Case #{}: {}'.format(tt, result))